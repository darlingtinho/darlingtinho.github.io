    <h1>About</h1>
    <p>minoHealth is a multifaceted system developed with
the objective of democratising Quality Healthcare with innovative and cutting edge
technologies like Artificial Intelligence, Data Science, Cloud computing and Biotechnology in
Africa. minoHealth and its AI Research lab, <a href="https://ai.minohealth.com" target="_blank">minoHealth AI Labs</a> researches and experiments
with Artificial Intelligence and ways it can be applied to Healthcare to make it faster, better
and yet, cheaper.</p><br><br>
    <h3>minoHealth: The multifaceted system</h3>
<p>minoHealth is a multifaceted system that combines Artificial Intelligence, Biotechnology(currently research), Big Data/Data Analysis and Cloud Computing in order to optimise processes involved in Healthcare and also make them affordable and scalable
compared to the current practices especially in Africa. minoHealth uses Artificial
Intelligence to make Health Predictions/Forecasts, Diagnoses and Prognoses and organises and stores Patient Health Records in the cloud with
‘Hospital portal’ and ‘Central Portal’ for authorised healthcare personnels. All Data
collected is intelligently organised with ‘Big Data’ approaches and technologies, they are
continually analysed so important health information/stats on health center patients are
visualised and made available to physicians.</p><br>

<p>At the same time, with the permission of the various health centers, all the health data are
also automatically saved and organised within the ‘Central Portal’ which allows for National
Health Records Databases. This could also be extended even for the continent. Analysed
and visualized health information/stats is a really good source of studying and noting early
patterns of outbreaks nationally or continentally, so they can be managed beforeescalation.
Collected data is used to further train and develop more Artificial Intelligence Healthcare models and systems.</p><br><br>
